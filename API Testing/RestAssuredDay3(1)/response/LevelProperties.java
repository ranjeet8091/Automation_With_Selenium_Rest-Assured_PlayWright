package response;

public class LevelProperties {
   String name;
   String caption;
   String type;
   PropertiesAnnotation annotations;
   
   
   
   public String getName() {
	return name;
   }
   public void setName(String name) {
	this.name = name;
   }
   public String getCaption() {
	return caption;
   }
   public void setCaption(String caption) {
	this.caption = caption;
   }
   public String getType() {
	return type;
   }
   public void setType(String type) {
	this.type = type;
   }
   public PropertiesAnnotation getAnnotations() {
	return annotations;
   }
   public void setAnnotations(PropertiesAnnotation annotations) {
	this.annotations = annotations;
   }
   
   
}
